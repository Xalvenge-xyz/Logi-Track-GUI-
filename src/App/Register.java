    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked

    String fname = name.getText();
    String lname = lastname1.getText();
    String address = adrs.getText();
    String email = eml.getText();
    String contact = Contact.getText();
    String rawPass = new String(password.getPassword());
    String confirmRaw = new String(confirmpass.getPassword());


    if (fname.isEmpty() || lname.isEmpty() || address.isEmpty() || email.isEmpty()
            || contact.isEmpty() || rawPass.isEmpty() || confirmRaw.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields");
        return;
    }

    if (!rawPass.equals(confirmRaw)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match");
        return;
    }

    try {
        config db = new config();

        String checkSql = "SELECT COUNT(*) FROM tbl_accounts WHERE acc_email=?";
        double count = db.getSingleValue(checkSql, email);

        if (count > 0) {
            JOptionPane.showMessageDialog(this, "Email is already in use. Please use another email.");
            return;
        }

        final JDialog loading = new JDialog(this, "Sending OTP...", true);
        JLabel lbl = new JLabel("Please wait, sending OTP to your email...");
        lbl.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        loading.add(lbl);
        loading.pack();
        loading.setLocationRelativeTo(this);


        SwingWorker<Boolean, Void> worker = new SwingWorker<Boolean, Void>() {
            String otp = config.generateOtp();

            @Override
            protected Boolean doInBackground() {
                return config.sendOtpEmail(email, otp);
            }

            @Override
            protected void done() {
                loading.dispose(); 
                try {
                    boolean sent = get();
                    if (!sent) {
                        JOptionPane.showMessageDialog(null, "Failed to send OTP. Please try again later.");
                        return;
                    }

                    
                    String inputOtp = JOptionPane.showInputDialog(null, "Enter the OTP sent to your email:");

                    if (inputOtp != null && inputOtp.equals(otp)) {
                        
                        String hashedPass = config.hashPassword(rawPass);

                        String sql = "INSERT INTO tbl_accounts(acc_name, acc_lname, acc_address, acc_email, acc_contact, acc_pass, acc_status, acc_role) "
                                   + "VALUES(?,?,?,?,?,?,1,'customer')";

                        db.addRecord(sql, fname, lname, address, email, contact, hashedPass);

                        JOptionPane.showMessageDialog(null, "Sign Up Successful! Your account is verified.");
                        dispose();
                        new Login().setVisible(true);

                    } else {
                        JOptionPane.showMessageDialog(null, "Incorrect OTP. Account creation failed.");
                    }

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
                }
            }
        };

        worker.execute();
        loading.setVisible(true);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jLabel3MouseClicked

